public class Index {
  private int elem0, elem1, elem2;

  public Index() {
  }
  
  public Index(int elem0, int elem1, int elem2) {
    this.elem0 = elem0;
    this.elem1 = elem1;
    this.elem2 = elem2;
  }

  public int getElem0() {
    return elem0;
  }
  
  public int getElem1() {
    return elem1;
  }
  
  public int getElem2() {
    return elem2;
  }

  public void setElem0(int elem0) {
    this.elem0 = elem0;
  }
  
  public void setElem1(int elem1) {
    this.elem1 = elem1;
  }
  
  public void setElem2(int elem2) {
    this.elem2 = elem2;
  }
}